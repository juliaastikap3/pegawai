	  		Yayasan Plan International Indonesia (YPII) merupakan organisasi pembangunan dan kemanusiaan yang memajukan hak-hak dan kesetaraan anak-anak untuk anak perempuan. Selama 75 tahun, Plan International telah membangun kemitraan yang kuat dan beroperasi pada lebih dari 70 negara.
			Di Indonesia, Plan International mulai beroperasi pada tahun 1969 dan saat ini telah bekerja di 7 provinsi dan mensponsori lebih dari 38.000 anak.
